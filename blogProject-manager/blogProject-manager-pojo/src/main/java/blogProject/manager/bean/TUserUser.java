package blogProject.manager.bean;

public class TUserUser {
    private Integer id;

    private Integer userFanId;

    private Integer userFollowedId;

    private Integer userId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUserFanId() {
        return userFanId;
    }

    public void setUserFanId(Integer userFanId) {
        this.userFanId = userFanId;
    }

    public Integer getUserFollowedId() {
        return userFollowedId;
    }

    public void setUserFollowedId(Integer userFollowedId) {
        this.userFollowedId = userFollowedId;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }
}